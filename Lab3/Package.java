class Package {
    private double width, height, length;
    Package() { // default constructor
    }
    Package(double w, double h, double l) {
        width = w; // a
        height = h; // b
        length = l; // c
    }
    double getVolume() {
        return width * length * height;
    }
    double getWidth() {
        return width;
    }
    double getHeight() {
        return height;
    }
    double getLength() {
        return length;
    }
    void setWidth(double w) {
        width = w;
    }
    void setHeight(double h) {
        height = h;
    }
    void setLength(double l) {
        length = l;
    }
    boolean isCube() {
        if ( (width == height) && (width == length) && (height == length) ) 
            return true;
        else
            return false;         
    }
}
